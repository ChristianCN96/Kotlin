fun main() {
    var dayOfWeek = 3

    when (dayOfWeek) {
        1 -> println("El dia de la semana es lunes")
        2 -> println("El dia de la semna es martes")
        3 -> println("El dia de la semana es miercoles")
        4 -> println("El dia de la semana es jueves")
        5 -> println("El dia de la semana es viernes")
        6 -> println("El dia de la semana es sabado")
        7 -> println("El dia de la semana es domingo")
        else -> println("No as seleccionado ningun dia")

    }

}